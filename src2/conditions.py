#variable x que guarda el numero 28
x = -28

#funciones condicionales
#si x es mayor que cero, imprimi "x es positive"
if x > 0:
    print("x is positive")
#en otro caso, si x es menor que cero, imprimi "x is negative"    
elif x < 0:
    print("x is negative")
#en cualquier otro caso, imprimi "x is zero"    
else:
    print("x is zero")
